package fundamentos.operadores

fun main(args: Array<String>) {
    var a: Int = 7;
    var b: Int = 3;

    // Atribuição aditiva
    b += a; // b = b + a;

    // Atribuição subtrativa
    b -= 4; // b = b - 4;

    // Atribuição multiplicativa
    b *= 2; // b = b * 2;

    // Atribuição divisiva
    b /= 2; // b = b / 2;

    // Atribuição por módulo
    b %= 2;

    println(b);
}